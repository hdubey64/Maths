// Print Sum of First 10 Even Number

var even = 0;

for (i = 0; i < 21; i += 2) {
   even = even + i;
}
console.log(`Sum Of First 10 Even Number: ${even}`);

// Print Sum of First 10 Odd Number

var odd = 1;

for (i = 0; i < 101; i += 2) {
   odd = odd + i;
}
console.log(`Sum Of First 10 Odd Number: ${odd}`);
